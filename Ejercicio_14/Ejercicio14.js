/*
Ejercicio 14
Crear un programa que al ingresar una nota de un alumno, muestre con un alert() si el alumno esta
“aprobado” (se aprueba con una nota mayor a 7) y “reprobado” en caso contrario
*/

let nota = prompt("Ingrese la nota: "); 

if (nota > 7) {
    alert("Aprobado");
} else {    
    alert("Reprobado");
}